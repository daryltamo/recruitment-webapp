// This file defines the FicheDePoste model for job descriptions.

const mongoose = require('mongoose');

const ficheDePosteSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    requirements: {
        type: [String],
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('FicheDePoste', ficheDePosteSchema);